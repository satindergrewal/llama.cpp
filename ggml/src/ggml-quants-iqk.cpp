// IQK quantization types: IQ4_K, IQ5_KS, IQ6_K and the trellis ("KT") family
// IQ1_KT, IQ2_KT, IQ3_KT, IQ4_KT.
//
// These quantization formats were designed and first implemented by
// Iwan Kawrakow (@ikawrakow) in ik_llama.cpp
// (https://github.com/ikawrakow/ik_llama.cpp). This file is a port of the
// reference implementations; block layouts, codebooks and the trellis
// generator are kept bit-exact with the original so that GGUF files are
// interchangeable between the two projects.
//
// The KT types use a "trellis" codebook: each group of 4 or 8 weights is
// represented by an index into a sequence generated by an integer LCG
// (x -> 0xCBAC1FED * x), taking the masked byte sum of the state as the
// value. There is no stored codebook - values are generated on the fly.
//
// IQ5_KS and all KT types store a per-row header (a single f32 scale) in
// front of the block data; see ggml_type_traits.row_meta_size.

