namespace {
// Correctness-first dot product: dequantize the row chunk and multiply with
// the (exactly representable) Q8_K quantized activations.
template <typename Block, void Dequant(const Block *, float *, int64_t)>
inline void iqk_vec_dot_q8_K_ref(int n, float * s, const void * vx, const void * vy) {
    GGML_ASSERT(n % QK_K == 0);
    thread_local std::vector<float> xf;
    xf.resize(n);
    Dequant((const Block *)vx, xf.data(), n);
    const block_q8_K * y = (const block_q8_K *)vy;
    const int nb = n / QK_K;
    float sumf = 0;
    for (int ib = 0; ib < nb; ++ib) {
        const float * xb = xf.data() + ib*QK_K;
        float sumb = 0;
        for (int j = 0; j < QK_K; ++j) {
            sumb += xb[j]*y[ib].qs[j];
        }
        sumf += y[ib].d*sumb;
    }
    *s = sumf;
}
} // namespace

